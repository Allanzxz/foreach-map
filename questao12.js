/*
Questão 12 - Qual é a diferença entre map() e forEach() em relação ao retorno 
de valores de uma função de callback?  
*/
//A diferença entre map() e forEach() em relação ao retorno de valores de uma função de callback é que o map() retorna um novo array com os resultados da função de callback aplicada a cada elemento do array original, enquanto o forEach() apenas itera sobre os elementos do array original, não retornando um novo array.