/*
Questão 17 - Qual é a diferença entre map() e forEach() em termos de 
manipulação do array original?  
*/
//A diferença entre map() e forEach() em termos de manipulação do array original é que o map() retorna um novo array com base nos resultados da função de callback aplicada a cada elemento do array original, enquanto o forEach() apenas itera sobre os elementos do array original sem retornar um novo array