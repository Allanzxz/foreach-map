/*
Questão 10 - É possível modificar o array original com map()?  
*/
//Não, o método map() não modifica o array original. Ele retorna um novo array com os resultados da função de callback aplicada a cada elemento do array original. Se você deseja modificar o array original, pode usar métodos como forEach() ou map() em combinação com a atribuição direta a cada elemento do array origin